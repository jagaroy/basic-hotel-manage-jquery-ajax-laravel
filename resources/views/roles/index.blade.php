    <script>
        var base_url = "{{ url('/') }}";
        var table = "";

        $(document).ready(function() {
            let ttitle = document.title;
            let messageTop = '';
            let messageBottom = '';

            var table = $('#data_table').on( 'error.dt', function ( e, settings, techNote, message ) {
                if(confirm('Please reload this page and check.')){
                    location.reload();
                }
            }).DataTable({
                fixedHeader: {
                    header: true,
                    headerOffset: $('#fixed').height()
                },
                searching: true,
                mark: true,
                dom: 'Blfrtip',
                buttons: [{
                    extend: 'print',
                    text: '<i class="fas fa-file-pdf"></i> Print/Save to PDF',
                    exportOptions: {
                        stripHtml: false, //for image show
                        columns: [0,1,2],
                    },
                    title: ttitle,
                    messageTop: function() {
                        return messageTop;
                    },
                    messageBottom: function() {
                        return '<h3 style="text-align:center">' + messageBottom + '</h3>';
                    },
                    customize: function(win) {

                        // this line is for centering messageTop
                        $(win.document.messageTop).css('text-align', 'center')

                        // for landscape print
                        var css =
                            '@page  { size: landscape; margin-top:30px; margin-botton:30px; }',
                            head = win.document.head || win.document.getElementsByTagName(
                                'head')[0],
                            style = win.document.createElement('style');
                        style.type = 'text/css';
                        style.media = 'print';
                        if (style.styleSheet) {
                            style.styleSheet.cssText = css;
                        } else {
                            style.appendChild(win.document.createTextNode(css));
                        }
                        head.appendChild(style);
                        // for landscape end

                    },
                },
                {
                    text: '<div class="bg-info"><i class="fas fa-file-pdf"></i> Print/Save to PDF All.</div>',
                    action: function(e, dt, node, config) {
                        dt.page.len(-1).draw();
                        dt.button('.buttons-print').trigger();
                    }
                }, 
                ],
                pageLength: 5,
                lengthMenu: [5, 10, 20, 50, 100, 200, 500, 1000, 100000],
                processing: true,
                serverSide: true,
                ajax: "{{ route('roles.index') }}",
                columns: [
                    {data: 'DT_RowIndex', name: 'DT_RowIndex'},

                    {data: 'name', name: 'name'},
					{data: 'description', name: 'description'},
					
                    
                    {data: 'action', name: 'action', orderable: false, searchable: false},
                ],

                drawCallback: function( settings ) {
                    // any js/jquery action after table draw
                    $(document).find('#data_table tr').each(function(index, el) {
                        let idd = $(el).closest('tr').find('.edit_btn').data('id');
                        $(el).addClass('row_' + idd);
                    });
                },
            });
            $('#add_form').submit(function(e) {
                e.preventDefault();
                var form_data = new FormData(this);

                $.ajax({
                    data: form_data,
                    url: "{{ route('roles.store') }}",
                    type: "POST",
                    cache: false,
                    contentType: false,
                    processData: false,
                    success: function(data) {
                        $.notify(data.message, data.status);
                        if (data.status == 'success') {
                            $('#add_form').trigger("reset");
                            $('#role-create-modal').modal('hide');
                            table.draw();
                        }
                    },
                    error: function(data) {
                        console.log('Error:', data);
                        if (data.status === 422) {
                            var response = JSON.parse(data.responseText);
                            var errors = response['errors'];
                            $.each(errors, function(key, val) {
                                $.notify(val[0], 'error');
                            });
                        }
                    }
                });
            });

            $('#edit_form').submit(function(e) {
                e.preventDefault();
                var role_id = $("#role_id").val();
                var form_data = new FormData(this);

                let _url = `roles/` + role_id;

                $.ajax({
                    data: form_data,
                    url: _url,
                    type: "POST",
                    cache: false,
                    contentType: false,
                    processData: false,
                    success: function(data) {
                        $.notify(data.message, data.status);
                        if (data.status == 'success') {

                            $('#edit_form').trigger("reset");
                            $('#role-edit-modal').modal('hide');
                            table.draw();
                        }
                    },
                    error: function(data) {
                        console.log('Error:', data);
                        if (data.status === 422) {
                            var response = JSON.parse(data.responseText);
                            var errors = response['errors'];
                            $.each(errors, function(key, val) {
                                $.notify(val[0], 'error');
                            });
                        }
                    }
                });
            });
        });

        function editRole(event) {
            $('#edit_form').trigger("reset");
            var id = $(event.target).data("id");
            let _url = 'roles/' + id;

            $.ajax({
                url: _url,
                type: "GET",
                success: function(response) {
                    if (response) {
                        $("#role_id").val(response.id);
                        
                        $('#edit_form #name').val(response.name);
						$('#edit_form #description').text(response.description);

                        $('#role-edit-modal').modal('show');
                    }
                }
            });
            table.draw();
        }

        function addRole() {

            $('#role-create-modal').modal('show');
            $('#add_form').trigger("reset");
        }
    </script>
